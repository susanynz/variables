
//calcular el perimetro de un circulo
function perimetroCirculo(radioCirculo) {
    return `El perimetro de un círculo es ${2 * 3.1416 * radioCirculo }`
}

 //calcular el area de un rectangulo
function perimetrorectangulo (base, _altura){
        return `El perimetro de un rectángulo es ${(2 * base) + (2 * _altura)}`
}

//calcular el cuadrado de un numero
function cuadradoNumerico (numero){
    return `El cuadrado de ${numero} es ${numero * numero}`
}
